package Day_1;

public class Switch {

	public static void main(String[] args) {
		String color="red";
				switch(color) {
				case "red":
					System.out.println("Celebrate holi");
					break;
				case "green":
					System.out.println("Celebrate ramzan");
					break;
				case "white":
					System.out.println("Celebrate Christmas");
					break;
				default:
					System.out.println("No festival");
				}
	}
}
